int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;
	int				diff;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			break ;
		i++;
	}
	diff = s1[i] - s2[i];
	return (diff);
}
